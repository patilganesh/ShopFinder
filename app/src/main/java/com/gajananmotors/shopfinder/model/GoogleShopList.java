public class GoogleShopList {
    String shopName;
    String shopAddress;
    String shopOpeningHours;
    String shopIcon;
    String shopLat;
    String shopLong;

    public String getShopName() {
        return shopName;
    }

    public void setShopName(String shopName) {
        this.shopName = shopName;
    }

    public String getShopAddress() {
        return shopAddress;
    }

    public void setShopAddress(String shopAddress) {
        this.shopAddress = shopAddress;
    }

    public String getShopOpeningHours() {
        return shopOpeningHours;
    }

    public void setShopOpeningHours(String shopOpeningHours) {
        this.shopOpeningHours = shopOpeningHours;
    }

    public String getShopIcon() {
        return shopIcon;
    }

    public void setShopIcon(String shopIcon) {
        this.shopIcon = shopIcon;
    }

    public String getShopLat() {
        return shopLat;
    }

    public void setShopLat(String shopLat) {
        this.shopLat = shopLat;
    }

    public String getShopLong() {
        return shopLong;
    }

    public void setShopLong(String shopLong) {
        this.shopLong = shopLong;
    }
}
